#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	srclen;

	srclen = ft_strlen(src);
	if (dstsize == 0)
		return (srclen);
	while ((*src != '\0') && (-- dstsize > 0))
		*dst ++ = *src ++;
	*dst = '\0';
	return (srclen);
}
